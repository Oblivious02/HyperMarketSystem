    private void UpdateInfoButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String[] rowData = new String[5];
        DefaultTableModel UserTableModel = (DefaultTableModel) UserTable.getModel();

        for (int i = 0; i < UserTableModel.getRowCount(); i++) {
            for (int j = 0; j < 5; j++) {
                rowData[j] = String.valueOf(UserTableModel.getValueAt(i, j));
            }
            String s = rowData[0] + ',' + rowData[1] + ',' + rowData[2] + ',' + rowData[3] + ',' + rowData[4];
            Boolean firstTime = true;
            for (User user: LoginFrame.Users) {
                String t = String.valueOf(user.getID()) + ',' + user.getUsername()  + ',' + user.getPassword() + ',' + user.getPhoneNumber() + ',' + user.getEmail();
                if (firstTime) {
                    FileOperations.write(t, "User.txt", false);
                    firstTime = false;
                }
                else FileOperations.write(t, "User.txt", true);
            }
            FileOperations.write(s, "User.txt", true);
        }

    }
